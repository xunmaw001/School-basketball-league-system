        async onLoad(options) {
            let qiuduiParams = {
                page: 1,
                limit: 100,
            }
            let qiuduiData = await this.$api.page(`qiudui`, qiuduiParams);
            this.qiuduiOptions = qiuduiData.data.list;
		/*下拉框*/
			let guojiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'guoji_types',
            }
			let guojiTypes = await this.$api.page(`dictionary`, guojiTypesParams);
			this.guojiTypesOptions = guojiTypes.data.list
		/*下拉框*/
			let qiuyuanTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'qiuyuan_types',
            }
			let qiuyuanTypes = await this.$api.page(`dictionary`, qiuyuanTypesParams);
			this.qiuyuanTypesOptions = qiuyuanTypes.data.list


