            },
            async onSubmitTap() {
				if ((!this.ruleForm.qiuyuanName)) {
                    this.$utils.msg(`球员名称不能为空`);
                    return
                }
				if ((!this.ruleForm.shangpinShengao) && (!this.$validate.isNumber(this.ruleForm.shangpinShengao)) && this.ruleForm.shangpinShengao >0) {
                    this.$utils.msg(`身高(米)不能为空，不能小于0 格式为数字或小数`);
                    return
                }
				if ((!this.ruleForm.shangpinTizhong) && (!this.$validate.isNumber(this.ruleForm.shangpinTizhong)) && this.ruleForm.shangpinTizhong >0) {
                    this.$utils.msg(`体重(kg)不能为空，不能小于0 格式为数字或小数`);
                    return
                }
				if ((!this.ruleForm.guojiTypes)) {
                    this.$utils.msg(`国籍不能为空`);
                    return
                }
				if ((!this.ruleForm.qiuyuanUuidNumber)) {
                    this.$utils.msg(`球员编号不能为空`);
                    return
                }
				if ((!this.ruleForm.qiuyuanShengri)) {
                    this.$utils.msg(`生日不能为空`);
                    return
                }
				if ((!this.ruleForm.qiuyuanPhoto)) {
                    this.$utils.msg(`球员照片不能为空`);
                    return
                }
				if ((!this.ruleForm.zanNumber) && (!this.$validate.isIntNumer(this.ruleForm.zanNumber)) && this.ruleForm.zanNumber >0) {
                    this.$utils.msg(`赞不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.caiNumber) && (!this.$validate.isIntNumer(this.ruleForm.caiNumber)) && this.ruleForm.caiNumber >0) {
                    this.$utils.msg(`踩不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.ruduiTime)) {
                    this.$utils.msg(`入队时间不能为空`);
                    return
                }
                // if(uni.getStorageSync('详情页传过来的Id')){
                //     this.ruleForm.zhuangxiuId = uni.getStorageSync('详情页传过来的Id')
                // }

                if (this.ruleForm.id) {
                    await this.$api.update(`qiuyuan`, this.ruleForm);
                } else {
                    await this.$api.save(`qiuyuan`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
