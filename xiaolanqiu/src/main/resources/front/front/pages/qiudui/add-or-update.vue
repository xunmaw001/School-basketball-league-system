    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					qiuduiUuidNumber: true,
					qiuduiName: false,
					qiuduiPhoto: false,
					qiuduiAddress: false,
					zanNumber: false,
					caiNumber: false,
					qiuduiTypes: false,
					qiuduiShenglv: false,
					qiuduiClicknum: false,
					qiuduiContent: false,
					insertTime: false,
					createTime: false,
        },
            ruleForm: {
					qiuduiUuidNumber: this.getUUID(),//数字
					qiuduiName: '',
					qiuduiPhoto: '',
					qiuduiAddress: '',
					zanNumber: '',
					caiNumber: '',
					qiuduiTypes: '',//数字
					qiuduiValue: '',//数字对应的值
					qiuduiShenglv: '',
					qiuduiClicknum: '',
					qiuduiContent: '',
