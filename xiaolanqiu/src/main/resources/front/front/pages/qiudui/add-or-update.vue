            },
            // 登陆用户信息
            user: {},
            qiuduiTypesOptions: [],
            qiuduiTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let qiuduiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'qiudui_types',
            }
			let qiuduiTypes = await this.$api.page(`dictionary`, qiuduiTypesParams);
			this.qiuduiTypesOptions = qiuduiTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`qiudui`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.qiuduiId){
                this.ruleForm.qiuduiId = options.qiuduiId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            qiuduiTypesChange(e) {
                this.qiuduiTypesIndex = e.target.value
                this.ruleForm.qiuduiValue = this.qiuduiTypesOptions[this.qiuduiTypesIndex].indexName
                this.ruleForm.qiuduiTypes = this.qiuduiTypesOptions[this.qiuduiTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			qiuduiPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.qiuduiPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.qiuduiUuidNumber)) {
                    this.$utils.msg(`球队编号不能为空`);
                    return
                }
				if ((!this.ruleForm.qiuduiName)) {
                    this.$utils.msg(`球队名称不能为空`);
                    return
                }
				if ((!this.ruleForm.qiuduiPhoto)) {
                    this.$utils.msg(`球队照片不能为空`);
                    return
                }
				if ((!this.ruleForm.qiuduiAddress)) {
                    this.$utils.msg(`球队位置不能为空`);
                    return
                }
				if ((!this.ruleForm.zanNumber) && (!this.$validate.isIntNumer(this.ruleForm.zanNumber)) && this.ruleForm.zanNumber >0) {
                    this.$utils.msg(`赞不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.caiNumber) && (!this.$validate.isIntNumer(this.ruleForm.caiNumber)) && this.ruleForm.caiNumber >0) {
                    this.$utils.msg(`踩不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.qiuduiShenglv)) {
                    this.$utils.msg(`胜率不能为空`);
                    return
                }
				if ((!this.ruleForm.qiuduiClicknum) && (!this.$validate.isIntNumer(this.ruleForm.qiuduiClicknum)) && this.ruleForm.qiuduiClicknum >0) {
                    this.$utils.msg(`球队热度不能为空，不能小于0 格式为数字`);
                    return
                }
                // if(uni.getStorageSync('详情页传过来的Id')){
                //     this.ruleForm.zhuangxiuId = uni.getStorageSync('详情页传过来的Id')
                // }

                if (this.ruleForm.id) {
                    await this.$api.update(`qiudui`, this.ruleForm);
                } else {
                    await this.$api.save(`qiudui`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
