            saishixinxiTypesOptions: [],
            saishixinxiTypesIndex : 0,
            shengliTypesOptions: [],
            shengliTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let saishixinxiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'saishixinxi_types',
            }
			let saishixinxiTypes = await this.$api.page(`dictionary`, saishixinxiTypesParams);
			this.saishixinxiTypesOptions = saishixinxiTypes.data.list
		/*下拉框*/
			let shengliTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'shengli_types',
            }
			let shengliTypes = await this.$api.page(`dictionary`, shengliTypesParams);
			this.shengliTypesOptions = shengliTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`saishixinxi`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.saishixinxiId){
                this.ruleForm.saishixinxiId = options.saishixinxiId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            saishixinxiTypesChange(e) {
                this.saishixinxiTypesIndex = e.target.value
                this.ruleForm.saishixinxiValue = this.saishixinxiTypesOptions[this.saishixinxiTypesIndex].indexName
                this.ruleForm.saishixinxiTypes = this.saishixinxiTypesOptions[this.saishixinxiTypesIndex].codeIndex
            },
            shengliTypesChange(e) {
                this.shengliTypesIndex = e.target.value
                this.ruleForm.shengliValue = this.shengliTypesOptions[this.shengliTypesIndex].indexName
                this.ruleForm.shengliTypes = this.shengliTypesOptions[this.shengliTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			// 日期控件
			bisiaTimeConfirm(val) {
                this.ruleForm.bisiaTime = val.result;
                this.$forceUpdate();
            },
			saishixinxiPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.saishixinxiPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.saishixinxiUuidNumber)) {
                    this.$utils.msg(`赛事信息编号不能为空`);
                    return
                }
				if ((!this.ruleForm.bisiaTime)) {
                    this.$utils.msg(`比赛时间不能为空`);
                    return
                }
				if ((!this.ruleForm.saishixinxiPhoto)) {
                    this.$utils.msg(`赛事照片不能为空`);
                    return
                }
				if ((!this.ruleForm.saishixinxiZhudui)) {
                    this.$utils.msg(`主队不能为空`);
                    return
                }
				if ((!this.ruleForm.saishixinxiKedui)) {
                    this.$utils.msg(`客队不能为空`);
                    return
                }
				if ((!this.ruleForm.saishixinxiChangdi)) {
                    this.$utils.msg(`比赛场地不能为空`);
                    return
                }
				if ((!this.ruleForm.shengliTypes)) {
                    this.$utils.msg(`胜利队伍不能为空`);
                    return
                }
                // if(uni.getStorageSync('详情页传过来的Id')){
                //     this.ruleForm.zhuangxiuId = uni.getStorageSync('详情页传过来的Id')
                // }

                if (this.ruleForm.id) {
                    await this.$api.update(`saishixinxi`, this.ruleForm);
                } else {
                    await this.$api.save(`saishixinxi`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
